Kelime = "Merhaba"

ilk_ucharf = Kelime[0:3] # 0. indeksten başlayarak 3. indekse kadar (3. indeks dahil değil)
print(ilk_ucharf) # Mer
son_ucharf = Kelime[-3:] # sondan 3 harfi alır
print(son_ucharf) # aba 
kelime_ters = Kelime[::-1] # baştan sona kadar -1 er atlayarak alır yani ters çevirir
print(kelime_ters) # abahreM

